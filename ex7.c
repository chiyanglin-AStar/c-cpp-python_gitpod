#include <stdio.h>

int main() {
    int a, b, product;
    a = 20;
    b = 7;

    printf("a = %d \n", a);
    printf("b = %d \n", b);
    
    // Calculating add 
    product = a + b; 
    printf("add = %d \n", product);

    // Calculating add 
    product = a - b; 
    printf("sub = %d \n", product);

    // Calculating product
    product = a * b; 
    printf("Product = %d \n", product);
    
    // Calculating div
    product = a / b; 
    printf("div = %d \n", product);

    return 0;
}
